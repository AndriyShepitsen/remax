				<h3 class="subheader">
					The 28/36 Rule
				</h3>
				<p>The 28/36 rule is an established benchmark used by many lenders to determine how much credit to offer you. Here's how it works:</p>
				<p>The "28" refers to the notion that no more than 28 percent of your gross monthly household income should go toward housing costs, which include mortgage principal, interest, taxes and insurance.</p>
				<p>To calculate, simply multiply your gross monthly income (amount before taxes) by .28. Use this amount as a guide for how much house you can afford.</p>
				<p>Example: You earn an annual salary of $70,000. Divide 70,000 by 12, giving you a monthly gross income of $5,833. Multiply that by .28, and you'll find you should spend no more than $1,633 each month on total housing costs.</p>
				<p>The "36" part of the 28/36 rule refers to your overall debt, which shouldn't exceed 36 percent of your income. This is important to consider because other high monthly debt loads – such as car and credit card payments – impact the amount you can afford to spend on housing.</p>
				<p>For first-time home buyers, the tricky part is knowing how much to budget for taxes and insurance. An experienced real estate professional can assist you with this.</p>
